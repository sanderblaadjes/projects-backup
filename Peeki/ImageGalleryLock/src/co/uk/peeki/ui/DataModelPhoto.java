package co.uk.peeki.ui;

public class DataModelPhoto {

	private int id;
	private String imagePath;
	private boolean blocked;
	private String date;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getImagePath() {
		return imagePath;
	}

	public void setImagePath(String imagePath) {
		this.imagePath = imagePath;
	}

	public boolean isBlocked() {
		return blocked;
	}

	public void setBlocked(boolean blocked) {
		this.blocked = blocked;
	}

	public DataModelPhoto() {
	}

	public DataModelPhoto(int id, String imagePath, boolean blocked, String date) {
		this.id = id;
		this.imagePath = imagePath;
		this.blocked = blocked;
		this.date = date;
	}

	public DataModelPhoto(String imagePath, boolean blocked, String date) {
		this.imagePath = imagePath;
		this.blocked = blocked;
		this.date = date;
	}

	@Override
	public String toString() {
		return "Photo [id=" + id + ", imagePath=" + imagePath + ", blocked="
				+ blocked + ", date=" + date + "]";
	}

}
