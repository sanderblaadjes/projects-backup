package com.android.cabapp.datastruct.json;

import com.google.gson.annotations.Expose;

public class Link {
	@Expose
	private String rel;
	@Expose
	private String uri;

	public String getRel() {
		return rel;
	}

	public void setRel(String rel) {
		this.rel = rel;
	}

	public String getUri() {
		return uri;
	}

	public void setUri(String uri) {
		this.uri = uri;
	}

}
