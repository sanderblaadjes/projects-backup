package co.uk.android.lldc.models;

public class EventMediaModel {

	String urlProfile = "";
	String name = "";
	Integer noOfLikes = 0;
	String urlBanner = "";
	String paginationUrl = "";

	public String getUrlProfile() {
		return urlProfile;
	}

	public void setUrlProfile(String urlProfile) {
		this.urlProfile = urlProfile;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getNoOfLikes() {
		return noOfLikes;
	}

	public void setNoOfLikes(Integer noOfLikes) {
		this.noOfLikes = noOfLikes;
	}

	public String getUrlBanner() {
		return urlBanner;
	}

	public void setUrlBanner(String urlBanner) {
		this.urlBanner = urlBanner;
	}

	/**
	 * @return the paginationUrl
	 */
	public String getPaginationUrl() {
		return paginationUrl;
	}

	/**
	 * @param paginationUrl
	 *            the paginationUrl to set
	 */
	public void setPaginationUrl(String paginationUrl) {
		this.paginationUrl = paginationUrl;
	}

}
