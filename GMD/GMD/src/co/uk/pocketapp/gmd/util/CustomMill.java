package co.uk.pocketapp.gmd.util;

public class CustomMill {

	int mill_id;
	String szPDFLink;
	String szFileName;

	public int getMill_id() {
		return mill_id;
	}

	public void setMill_id(int mill_id) {
		this.mill_id = mill_id;
	}

	public String getPDFLink() {
		return szPDFLink;
	}

	public void setPDFLink(String szPDFLink) {
		this.szPDFLink = szPDFLink;
	}

	public String toString() {

		return "Mill ID = " + mill_id + ", " + "PDF = " + szPDFLink;

	}

	public String getFileName() {
		return szFileName;
	}

	public void setFileName(String szFileName) {
		this.szFileName = szFileName;
	}

}
