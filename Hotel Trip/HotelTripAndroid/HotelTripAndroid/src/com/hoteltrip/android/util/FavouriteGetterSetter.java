package com.hoteltrip.android.util;

import android.graphics.Bitmap;

public class FavouriteGetterSetter {

	String hotelname;
	String hoteladdress;
	String rating;
	Float price;
	String imgpath;
	String hotelid;
	String reviewcount;
	String reviewimageurl;

	String currency;

	public FavouriteGetterSetter() {

	}

	public String getCurrency() {
		return currency;
	}

	public void setCurrency(String currency) {
		this.currency = currency;
	}

	// constructor
	public FavouriteGetterSetter(String id, String name, String img,
			String address, String reviews, Float price, String reviewcount,
			String reviewimageurl) {
		this.hotelid = id;
		this.hotelname = name;
		this.imgpath = img;
		this.hoteladdress = address;
		this.rating = reviews;
		this.price = price;
		this.reviewcount = reviewcount;
		this.reviewimageurl = reviewimageurl;
	}

	public String getRating() {
		return rating;
	}

	public void setRating(String rating) {
		this.rating = rating;
	}

	public String getReviewcount() {
		return reviewcount;
	}

	public void setReviewcount(String reviewcount) {
		this.reviewcount = reviewcount;
	}

	public String getReviewimageurl() {
		return reviewimageurl;
	}

	public void setReviewimageurl(String reviewimageurl) {
		this.reviewimageurl = reviewimageurl;
	}

	public String getHotelid() {
		return hotelid;
	}

	public void setHotelid(String hotelid) {
		this.hotelid = hotelid;
	}

	public String getHotelname() {
		return hotelname;
	}

	public void setHotelname(String hotelname) {
		this.hotelname = hotelname;
	}

	public String getHoteladdress() {
		return hoteladdress;
	}

	public void setHoteladdress(String hoteladdress) {
		this.hoteladdress = hoteladdress;
	}

	public Float getPrice() {
		return price;
	}

	public void setPrice(Float price) {
		this.price = price;
	}

	public String getImgpath() {
		return imgpath;
	}

	public void setImgpath(String imgpath) {
		this.imgpath = imgpath;
	}

}
